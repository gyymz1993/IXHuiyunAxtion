package com.huiyun.ixhuiyunaxtion.master.dao.database;

/**
 * 场景表的Bean(废弃)
 *
 */
public class SceneBean {
	private int scene;
	private int type;
	private int addr;
	private int obj;
	private int val;
	public int getScene() {
		return scene;
	}
	public void setScene(int scene) {
		this.scene = scene;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public int getAddr() {
		return addr;
	}
	public void setAddr(int addr) {
		this.addr = addr;
	}
	public int getObj() {
		return obj;
	}
	public void setObj(int obj) {
		this.obj = obj;
	}
	public int getVal() {
		return val;
	}
	public void setVal(int val) {
		this.val = val;
	}
}
