package com.huiyun.ixhuiyunaxtion.master.bean.json;
/**
 * 用于普通的Json对象
 * 
 * @author lzy_torah
 * 
 */
public class BaseJsonObj {
	public String obj;
	public int code;
	public int result;

	public String getObj() {
		return obj;
	}

	public void setObj(String obj) {
		this.obj = obj;
	}

	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}

	public int getResult() {
		return result;
	}

	public void setResult(int result) {
		this.result = result;
	}

}
